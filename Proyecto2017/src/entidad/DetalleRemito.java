package entidad;

public class DetalleRemito {
private int codigo_os;
private String nombre_os;
private int cant_sesionesmensuales;
public int getCodigo_os() {
	return codigo_os;
}
public void setCodigo_os(int codigo_os) {
	this.codigo_os = codigo_os;
}
public String getNombre_os() {
	return nombre_os;
}
public void setNombre_os(String nombre_os) {
	this.nombre_os = nombre_os;
}
public int getCant_sesionesmensuales() {
	return cant_sesionesmensuales;
}
public void setCant_sesionesmensuales(int cant_sesionesmensuales) {
	this.cant_sesionesmensuales = cant_sesionesmensuales;
}

}
